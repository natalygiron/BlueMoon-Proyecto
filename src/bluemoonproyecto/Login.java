    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement ps;
        ResultSet rs;

        String email = txtEmail.getText();
        String password = txtPassword.getText();

        try {

            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT EMAIL_USUARIO, CONTRASEÑA_USUARIO, TIPO_USUARIO FROM USUARIO WHERE EMAIL_USUARIO =?");
            ps.setString(1, email);
            rs = ps.executeQuery();
            
            
            if(rs.next() == false) {
                JOptionPane.showMessageDialog(null, "Usuario o pssword incorrecto.","Error", JOptionPane.ERROR_MESSAGE);
            }else{
                do{
                    if(email.equals(rs.getString("EMAIL_USUARIO")) && password.equals(rs.getString("CONTRASEÑA_USUARIO"))){
                        String tipoUsuario = rs.getString("TIPO_USUARIO");
                        switch(tipoUsuario){
                            case "admin": admin.setVisible(true); break;
                            case "mozo": mozo.setVisible(true); break;
                            case "cajero": cajero.setVisible(true); break;
                        }
                    }
                }while(rs.next());  
            }

            //System.out.println(rs);

        } catch (Exception e) {
            System.out.println("ERROR");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

